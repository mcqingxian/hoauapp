package com.hoau.hoauapp.vo;

/**
 * 优惠券信息实体
 * 
 * @author 277610
 * @Date 2015-04-02
 */
public class CouponClippingVo extends ResBaseVo {

	/**
	 * 序列化
	 */
	private static final long serialVersionUID = 8907663480373724820L;
	
	/**
     * 主键
     */
    private int id;

	/**
	 * 优惠券金额
	 */
	private int amount;

	/**
	 * 积分数
	 */
	private int integral;

	public int getAmount() {
		return amount;
	}

	public void setAmount(int amount) {
		this.amount = amount;
	}

	public int getIntegral() {
		return integral;
	}

	public void setIntegral(int integral) {
		this.integral = integral;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

}
