package com.hoau.hoauapp.vo;

public class PriceAndTimeResponse extends ResBaseVo{
    protected String ebpdDeliveryHour;
    protected String ebpdSendHour;
    protected double freightPrice;
    protected double insurancePrice;
    protected String isSuccess;
    protected String msg;
	public String getEbpdDeliveryHour() {
		return ebpdDeliveryHour;
	}
	public void setEbpdDeliveryHour(String ebpdDeliveryHour) {
		this.ebpdDeliveryHour = ebpdDeliveryHour;
	}
	public String getEbpdSendHour() {
		return ebpdSendHour;
	}
	public void setEbpdSendHour(String ebpdSendHour) {
		this.ebpdSendHour = ebpdSendHour;
	}
	public String getIsSuccess() {
		return isSuccess;
	}
	public void setIsSuccess(String isSuccess) {
		this.isSuccess = isSuccess;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public double getFreightPrice() {
		return freightPrice;
	}
	public void setFreightPrice(double freightPrice) {
		this.freightPrice = freightPrice;
	}
	public double getInsurancePrice() {
		return insurancePrice;
	}
	public void setInsurancePrice(double insurancePrice) {
		this.insurancePrice = insurancePrice;
	}

	
}
